'use strict';

//  modulus operator basically returns the division remainder

let x = 7;
x = x % 3;
console.log(x); // the value of x will be 1 because the remainder of 7 divided by 3 is 1.

let y = 21;
y = y % 4;
console.log(y); // the value of y will be 1 because the remainder of 21 divided by 4 is 1.

let z = 13;
z = z % 2;
console.log(z); // the value of z will be 1 because the remainder of 13 divided by 2 id 1.
